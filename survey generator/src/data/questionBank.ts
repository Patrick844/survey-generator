/**
 * Builds a draft in the exact shape the "create new" form uses, so a predefined
 * item is indistinguishable from a hand-authored question downstream (same
 * `Question` -> `BackendQuestion` -> DB path). All driver items are Likert 1-5,
 * hence `type: "rating"`, `min_value: 1`, `max_value: 5`, no options.
 */
export function bankItemToDraft(category: string, statement: string): QuestionDraft {
  return {
    category,
    question: statement,
    type: "rating",
    options: [],
    metadata: {
      min_value: LIKERT_MIN,
      max_value: LIKERT_MAX,
    },
  };
}
